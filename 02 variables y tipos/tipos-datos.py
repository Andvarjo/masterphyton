nada = None #tipo de dato vacio (none)
print(type(nada))# con type me arroja el tipo de dato
cadena="hola soy andres varela" #tipo string (str)
print(type(cadena))
entero=25 #variable tipo entero (int)
print(type(entero)) 
flotante=37.5    #variable tipo entero (float)
print(type(flotante))
boolean=True #solo indica True/False (bool) debe llevar mayuscula al inicio
print(type(boolean))
array=[1,5,6,8,9] # variable tipo array o lista (list)
print(type(array))
arraystring=[44,"treinta",30,"cuarenta"] #tipo de dato list pero con strings
print(type(arraystring))
tupla=("master","en","python") #lista de datos que no cambian
print(type(tupla))
diccionario= {                  #variable tipo diccionario coleccion de datos de clave y valor (dict)
    "nombre": "andres",         # se debe terminar con coma (,)
    "apellido": "varela",
    "curso": "master en python"
} 
print(type(diccionario))
rango= range(9)   #variable tipo rango (range)
print(type(rango))
datobyte=b"probando"  #tipo de dato (bytes)
print(type(datobyte))
